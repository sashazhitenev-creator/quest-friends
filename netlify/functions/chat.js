const MODEL = "llama-3.3-70b-versatile";
const FALLBACK_MODEL = "llama-3.1-8b-instant"; // отдельный дневной лимит — включается при 429
const MAX_HISTORY = 24; // последние ~12 ходов держим целиком
const MAX_ACTION_LEN = 500; // защита от гигантских сообщений

// Инструкция для сжатия памяти (режим summarize)
const SUMMARY_PROMPT = `Ты — система памяти текстовой игры. Тебе дают предыдущую сводку и последние ходы игры. Составь ОБНОВЛЁННУЮ краткую сводку (до 200 слов) на русском, которая сохраняет ВСЁ важное для продолжения истории: кто такой игрок и что с ним произошло, какую цель он раскрыл или к чему движется, ключевые персонажи и отношения с ними, важные предметы в инвентаре, незакрытые зацепки и тайны, текущая ситуация. Пиши сжато, по фактам, без художественных описаний. Только сама сводка, без вступлений.`;

// Универсальный вызов Groq с фолбэком на запасную модель при лимите 429
async function callGroqRaw(apiKey, systemPrompt, messages, maxTokens, temperature) {
  async function ask(model) {
    const resp = await fetch("https://api.groq.com/openai/v1/chat/completions", {
      method: "POST",
      headers: { "Content-Type": "application/json", Authorization: "Bearer " + apiKey },
      body: JSON.stringify({
        model: model,
        max_tokens: maxTokens,
        temperature: temperature,
        messages: [{ role: "system", content: systemPrompt }, ...messages],
      }),
    });
    const data = await resp.json();
    return { resp, data };
  }
  let r = await ask(MODEL);
  if (r.resp.status === 429) r = await ask(FALLBACK_MODEL);
  if (!r.resp.ok) {
    const msg = r.data && r.data.error && r.data.error.message ? r.data.error.message : "Groq вернул ошибку " + r.resp.status;
    throw new Error(msg);
  }
  const text = r.data.choices && r.data.choices[0] && r.data.choices[0].message ? r.data.choices[0].message.content : "";
  return { text };
}

exports.handler = async function (event) {
  // CORS: игра на Яндекс Играх/VK живёт на чужом домене и обращается к этой функции
  const CORS = {
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Headers": "Content-Type",
    "Access-Control-Allow-Methods": "POST, OPTIONS",
  };

  if (event.httpMethod === "OPTIONS") {
    return { statusCode: 204, headers: CORS, body: "" };
  }
  if (event.httpMethod !== "POST") {
    return { statusCode: 405, headers: CORS, body: JSON.stringify({ error: "Only POST" }) };
  }

  const apiKey = process.env.GROQ_API_KEY;
  if (!apiKey) {
    return { statusCode: 500, headers: CORS, body: JSON.stringify({ error: "GROQ_API_KEY не задан на сервере" }) };
  }

  let body;
  try {
    body = JSON.parse(event.body || "{}");
  } catch (e) {
    return { statusCode: 400, headers: CORS, body: JSON.stringify({ error: "Некорректный JSON" }) };
  }

  let messages = Array.isArray(body.messages) ? body.messages : [];

  // Валидация: только role user/assistant, строки, обрезаем длину
  messages = messages
    .filter((m) => m && (m.role === "user" || m.role === "assistant") && typeof m.content === "string")
    .slice(-MAX_HISTORY)
    .map((m) => ({ role: m.role, content: m.content.slice(0, m.role === "user" ? MAX_ACTION_LEN : 4000) }));

  if (messages.length === 0) {
    return { statusCode: 400, headers: CORS, body: JSON.stringify({ error: "Пустая история" }) };
  }

  // ── Режим сжатия памяти: фронт присылает mode:"summarize" + старые ходы, получает сводку ──
  if (body.mode === "summarize") {
    const prev = typeof body.summary === "string" ? body.summary : "";
    const userContent = "Предыдущая сводка:\n" + (prev || "(пока нет)") +
      "\n\nПоследние ходы игры:\n" +
      messages.map((m) => (m.role === "user" ? "Игрок: " : "Игра: ") + m.content).join("\n");
    try {
      const r = await callGroqRaw(apiKey, SUMMARY_PROMPT, [{ role: "user", content: userContent }], 400, 0.3);
      const text = r.text || prev;
      return { statusCode: 200, headers: Object.assign({ "Content-Type": "application/json" }, CORS), body: JSON.stringify({ summary: text.trim() }) };
    } catch (e) {
      return { statusCode: 502, headers: CORS, body: JSON.stringify({ error: "Сбой суммаризации: " + e.message }) };
    }
  }

  // Режим повествования: detailed (по умолчанию) или simple
  const mode = body.mode === "simple" ? "simple" : "detailed";
  // Тип игры: story (по умолчанию) или endless
  const gameType = body.gameType === "endless" ? "endless" : "story";
  // Хардкор 18+ (передаётся только friends-версией)
  const hardcore = body.hardcore === true;
  // Сводка ранних событий (сжатая память длинной партии)
  const summary = typeof body.summary === "string" && body.summary.trim() ? body.summary.trim() : "";

  let systemPrompt = SYSTEM_PROMPT + "\n\n" + GAME_TYPES[gameType] + "\n\n" + STYLES[mode];
  if (hardcore) systemPrompt += "\n\n" + HARDCORE;
  if (summary) systemPrompt += "\n\nПАМЯТЬ О ПРОИЗОШЕДШЕМ РАНЕЕ (учитывай это как случившееся):\n" + summary;
  if (mode === "simple") {
    systemPrompt += "\n\n⚠️ НАПОМИНАНИЕ: ответ — МАКСИМУМ 1-2 коротких предложения, только результат, без описаний.";
  }
  const maxTokens = mode === "simple" ? 120 : 500;

  try {
    // хардкор чуть «горячее» для разнообразия речи, но не слишком
    const temp = hardcore ? 0.9 : 0.8;
    const r = await callGroqRaw(apiKey, systemPrompt, messages, maxTokens, temp);

    let text = r.text || "";
    // В простом режиме жёсткий лимит токенов может оборвать фразу на полуслове.
    // Если STATUS/FINAL нет (значит обрезалось) и текст не кончается точкой —
    // подрезаем до последнего законченного предложения.
    if (mode === "simple" && !/STATUS:|FINAL:/.test(text)) {
      const trimmed = text.trim();
      if (trimmed && !/[.!?…]$/.test(trimmed)) {
        const lastStop = Math.max(trimmed.lastIndexOf("."), trimmed.lastIndexOf("!"), trimmed.lastIndexOf("?"));
        if (lastStop > 0) text = trimmed.slice(0, lastStop + 1);
      }
    }

    return {
      statusCode: 200,
      headers: Object.assign({ "Content-Type": "application/json" }, CORS),
      body: JSON.stringify({ text: text }),
    };
  } catch (e) {
    return { statusCode: 502, headers: CORS, body: JSON.stringify({ error: "Сбой запроса к Groq: " + e.message }) };
  }
};
